export const getPluralSuffix = (num: number): number => {
  if (num == 0) {
    return 0
  }

  const num10 = num % 10,
    num100 = num % 100

  return num10 == 1 && num100 != 11 ? 1 : num10 >= 2 && num10 <= 4 && (num100 < 10 || num100 >= 20) ? 2 : 3
}
